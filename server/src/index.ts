import cors from "cors";
import "dotenv/config";
import express from "express";
import Controller from "./controllers"
import { UserRecommendation } from "./types/UserRecommendationInterface";

class CodingChallenge {
  private port = process.env.PORT;
  public app: express.Application;
  private ctrl = new Controller();
  constructor() {
    this.app = express();
    this.config();
    this.routes()
  }

  public start(): void {
    this.app.listen(this.port, () =>
      console.log(`Example app listening on port ${this.port}!`)
    );
  }
  public routes(): void {
    this.app.get("/api", (req, res) => this.ctrl.getRecommendation().then((result: UserRecommendation[]) => res.send(result)));
    this.app.post("/api", (req, res) => this.ctrl.postRecommendation(req.body).then((createdRec) => res.send(createdRec)))
  }

  public config() {
    this.app.set("port", this.port);
    this.app.use(express.json());
    this.app.use(express.urlencoded({ extended: false }));
    this.app.use(cors());
  }
}

const codingChallenge = new CodingChallenge();
codingChallenge.start();
